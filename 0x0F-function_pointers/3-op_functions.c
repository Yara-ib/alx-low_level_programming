#include "3-calc.h"

/**
 * op_add - sum of a and b.
 * @a: first integer.
 * @b: second integer.
 * Return: sum.
*/

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtraction of a and b.
 * @a: first integer.
 * @b: second integer.
 * Return: subtraction.
*/

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - product of a and b.
 * @a: first integer.
 * @b: second integer.
 * Return: result.
*/

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - division of a by b.
 * @a: first integer.
 * @b: second integer.
 * Return: result.
*/

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - remainder of the division of a by b.
 * @a: first integer.
 * @b: second integer.
 * Return: result.
*/

int op_mod(int a, int b)
{
	return (a % b);
}
